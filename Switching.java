
public class Switching
{
	 public static void main(String[] args) {
	        int color = 3; 

	        switch (color)
	        {
	            case 1:
	                System.out.println("Red");
	                break;
	            case 2:
	                System.out.println("Orange");
	                break;
	            case 3:
	                System.out.println("Green");
	                break;
	            case 4:
	                System.out.println("Yellow");
	                break;
	            default:
	                System.out.println("White");
	        }
}
}
